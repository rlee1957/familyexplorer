Rank
	What is BUCK$ system Rank?
		The count of team members when a member signes up in the BUCK$ system using the team promo code. 1st individual to sign up in the BUCK$ system using the team promo code is rank(1). The 10th individual to sign up in the BUCK$ system using the team promo code is rank(10). The 999th individual to sign up in the BUCK$ system using the team promo code is rank(999) etc...
	Rank Determines Position
		If you are the 1st, 11th, 21st, 31st etc... you will be position 1.
		If you are the 2nd, 12th, 22nd, 32nd etc... you will be position 2.
		If you are the 3rd, 13th, 23rd, 33rd etc... you will be position 3.
		If you are the 4th, 14th, 24th, 34th etc... you will be position 4.
		If you are the 5th, 15th, 25th, 35th etc... you will be position 2.
		If you are the 6th, 16th, 26th, 36th etc... you will be position 3.
		If you are the 7th, 17th, 27th, 37th etc... you will be position 4.
		If you are the 8th, 18th, 28th, 38th etc... you will be position 3.
		If you are the 9th, 19th, 29th, 39th etc... you will be position 4.
		If you are the 10th, 20th, 30th, 40th etc... you will be position 4.
