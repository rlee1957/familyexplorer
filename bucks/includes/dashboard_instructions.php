<li>You will not have access to the invitation correspondence links until you
verify that you have sent payment to all of your sponsors.</li>
<li>
  If you are a new member, then you will want to acquire team members. To 
  acquire team members, you must invite them. Click the <u>Correspondence</u> link. 
  Click the <u>Sample Invitations</u> link and print the page to send
  through the mail. Click <u>Sample email</u> to email 
  invitations. Click the <u>Sample flyer</u> link to print and pass out fliers.
  The key is the URL to the website and the Sponsor ID. The URL is to the BUCKS website and the
  Sponsor ID is your BUCKS Member ID. When an individual accepts the invitation
  and Signs Up as a member of BUCKS, you become their sponsor and they will
  send payments to you. They are part of your member team.  <br />
</li>
<li>
  After you have sent invitations and have acquired team members, you will receive
  payments in the mail. For each payment received click the <u>Verify Received 
  Payments</u> link. Enter the ID provided with the payment. Click the Check 
  button. The Name and Address of the member will display. Click the Payment 
  Received checkbox and click the Save button.   <br />
</li>
<li>
  Continue to send invitations and verify receipt of payments.
</li>

